# Замена _process_query в test_agent.py (исправлены имя инструмента + аргументы)
def _process_query(self, state: AgentState) -> AgentState:
    system_prompt = SystemMessage(content=(
        "Ты — Агата, агент для создания аналитических дайджестов в PowerPoint.\n"
        "Когда пользователь просит сделать презентацию/дайджест по Excel-файлу — "
        "ВЫЗОВИ инструмент generate_digest_pptx. Не пиши содержимое слайдов сам: "
        "инструмент строит всё из данных файла.\n"
        "Как заполнять аргументы:\n"
        "• xlsx_path — путь к файлу из запроса пользователя (дословно).\n"
        "• style_prompt — пожелания по стилю; по умолчанию 'Фирменный стиль SberF1'.\n"
        "• force_theme='sberf1' — для фирменного бренд-оформления (фон, шрифт, иконки).\n"
        "• layout='overview' — если пользователь просит плотный обзорный слайд, "
        "ИЛИ если файл с листами 'исх'+'динамика'. В остальных случаях не указывай "
        "(будет классический дайджест). Режим всё равно определится по листам сам.\n"
        "• period и issue_number — если пользователь их назвал (для шапки обзора).\n"
        "Если пользователь просто общается (не про презентацию) — отвечай обычным текстом, "
        "без вызова инструментов."
    ))
    messages_to_send = [system_prompt] + state["messages"]
    response = self.model.invoke(messages_to_send)
    return {"messages": [response]}
